import asyncio
from edge_tts import Communicate

TEXT = """
مرحباً بكم يا أبطال المستقبل في درس اليوم عن النظام الشمسي.

يتكون نظامنا الشمسي من نجم مركزي واحد، وهو الشمس، وكل ما يدور حولها من كواكب، وأقمار، وكويكبات، ومذنبات.

الشمس هي قلب النظام، وهي كرة ضخمة من الغازات الملتهبة تمدنا بالضوء والحرارة اللازمين للحياة على كوكب الأرض. بدون جاذبية الشمس، لن يكون هناك نظام شمسي!

هناك ثمانية كواكب في نظامنا الشمسي مقسمة إلى مجموعتين:

أولاً: الكواكب الداخلية وهي صخرية وصغيرة، وتشمل عطارد، وهو الكوكب الأقرب للشمس؛ والزهرة، أشد الكواكب حرارة؛ والأرض، كوكبنا المائي؛ والمريخ، الكوكب الأحمر.

ثانياً: الكواكب الخارجية وهي غازية وعملاقة، وتشمل المشتري، وهو أكبر الكواكب حجماً؛ وزحل، المشهور بحلقاته الرائعة؛ وأورانوس، الكوكب الجليدي البارد؛ ونبتون، وهو أبعد الكواكب عن الشمس.

وفي الختام، تذكروا أن كل كوكب في نظامنا الشمسي فريد من نوعه، وما يزال أمامنا الكثير لنستكشفه في هذا الفضاء الواسع.

شكراً لاستماعكم، وأتمنى لكم رحلة تعلم ممتعة!
"""

VOICE = "ar-SA-HamedNeural"
OUTPUT_FILE = r"d:\work\Global-Smart-Education-Platform\assets\audio\solar_system.mp3"

async def amain() -> None:
    communicate = Communicate(TEXT, VOICE)
    await communicate.save(OUTPUT_FILE)
    print(f"Success: Audio generated using {VOICE} and saved to {OUTPUT_FILE}")

if __name__ == "__main__":
    asyncio.run(amain())
